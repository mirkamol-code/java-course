package com.mirkamolcode;

public class Main {

    static void main(String[] args) {
      // Comparison operators
      // <, <=, >, >=, ==, !=
        System.out.println(10 > 10); // the output will be false
        System.out.println(10 >= 10); // the output will be true
        System.out.println(10 < 10); // the output will be false
        System.out.println(10 <= 10); // the output will be true
        System.out.println(10 == 10); // the output will be true
        System.out.println(10 != 10); // the output will be false

        System.out.println();
        boolean isAdult = 15 >= 16;
        System.out.println(isAdult);
    }
}
